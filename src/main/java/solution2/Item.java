package solution2;

public class Item {

    private String code;
    private String type;
    private String name;
    private double price;

    void setCode(String code) {
        this.code = code;
    }

    String getCode() {
        return code;
    }

    void setType(String type) {
        this.type = type;
    }

    String getType() {
        return type;
    }

    void setName(String name) {
        this.name = name;
    }

    String getName() {
        return name;
    }

    void setPrice(double price) {
        this.price = price;
    }

    double getPrice() {
        return price;
    }

}
